package com.sinau.simda.domain;

public class MusrembangKabupatenView {
	private String tahun;
	private String skpd_kode;
	private String kecamatan_kode;
	private String deskel_kode;
	private String kegiatan;
	private String tipe_kode;

	public String getTahun() {
		return tahun;
	}

	public void setTahun(String tahun) {
		this.tahun = tahun;
	}

	public String getSkpd_kode() {
		return skpd_kode;
	}

	public void setSkpd_kode(String skpd_kode) {
		this.skpd_kode = skpd_kode;
	}

	public String getKecamatan_kode() {
		return kecamatan_kode;
	}

	public void setKecamatan_kode(String kecamatan_kode) {
		this.kecamatan_kode = kecamatan_kode;
	}

	public String getDeskel_kode() {
		return deskel_kode;
	}

	public void setDeskel_kode(String deskel_kode) {
		this.deskel_kode = deskel_kode;
	}

	public String getKegiatan() {
		return kegiatan;
	}

	public void setKegiatan(String kegiatan) {
		this.kegiatan = kegiatan;
	}

	public String getTipe_kode() {
		return tipe_kode;
	}

	public void setTipe_kode(String tipe_kode) {
		this.tipe_kode = tipe_kode;
	}

}
